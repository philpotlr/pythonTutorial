#String Functions

myStr = 'Hello World'

#Capitalise
print(myStr.capitalize())


#Swap Case
print(myStr.swapcase())


#Length
print(len(myStr))


#Replace
print(myStr.replace('World', 'Everyone'))

sub = 'l'

# count
print(myStr.count(sub))


# starts with
print(myStr.startswith('Hello'))


#ends with
print(myStr.endswith('World'))


# Split to list
print(myStr.split())


# Find
print(myStr.find('World'))


# Index
print(myStr.index('l'))


# is all alphanumeric
print(myStr.isalnum())


# is all alphabetic
print(myStr.isalpha())


# is all numeric
print(myStr.isnumeric())

